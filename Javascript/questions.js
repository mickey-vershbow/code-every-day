//////////////////////////////////////////////////////////////////////////
// Write a recursive function that counts down from a given number.
//////////////////////////////////////////////////////////////////////////


const countDown = (n) => {
    console.log(n);

    let nextNumber = n - 1;

    while (nextNumber > 0) {
        console.log(countDown(nextNumber));
    }
}

console.log(countDown(5))

